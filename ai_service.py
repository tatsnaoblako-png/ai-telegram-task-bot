def get_ai_response(text):
    text = text.lower()

    # 💰 деньги / работа
    if "зараб" in text or "деньг" in text or "доход" in text:
        return (
            "💰 Цель:\n"
            "Выйти на доход 300–500k+\n\n"
            "🔍 Анализ:\n"
            "Сейчас нет чёткой стратегии роста или масштабирования дохода\n\n"
            "🛠 План:\n"
            "1. Определи текущие навыки\n"
            "2. Выбери направление с высоким доходом (IT, аналитика, продукт)\n"
            "3. Найди 1–2 источника дохода\n"
            "4. Начни активно откликаться или брать заказы\n\n"
            "⚡ Быстрые шаги:\n"
            "- Обнови резюме\n"
            "- Откликнись на 10 вакансий\n"
            "- Посмотри фриланс-площадки"
        )

    # 📚 обучение
    elif "уч" in text or "навык" in text or "курс" in text:
        return (
            "📚 Цель:\n"
            "Освоить новый навык\n\n"
            "🔍 Анализ:\n"
            "Без системы обучение даёт слабый результат\n\n"
            "🛠 План:\n"
            "1. Выбери 1 конкретный навык\n"
            "2. Учись каждый день 1–2 часа\n"
            "3. Делай практические проекты\n\n"
            "⚡ Быстрые шаги:\n"
            "- Найди курс\n"
            "- Начни сегодня\n"
            "- Сделай первый мини-проект"
        )

    # 🚀 развитие
    elif "разв" in text or "рост" in text:
        return (
            "🚀 Цель:\n"
            "Личностный и карьерный рост\n\n"
            "🔍 Анализ:\n"
            "Нет чёткого фокуса и системы действий\n\n"
            "🛠 План:\n"
            "1. Определи цель на 3 месяца\n"
            "2. Разбей на шаги\n"
            "3. Действуй ежедневно\n\n"
            "⚡ Быстрые шаги:\n"
            "- Запиши цель\n"
            "- Сделай первый шаг сегодня"
        )

    # 👋 привет
    elif "привет" in text:
        return "Привет! 👋 Напиши свою задачу — помогу разобрать"

    # 🌦 погода
    elif "погод" in text:
        return "Я пока не умею смотреть погоду 🌦, но могу помочь с целями и задачами 🚀"

    # 🤔 fallback
    else:
        return (
            "🤔 Давай разберём твою задачу\n\n"
            "Попробуй уточнить:\n"
            "- какая цель?\n"
            "- какие ограничения?\n"
            "- что уже пробовал?\n\n"
            "Я помогу составить план 🚀"
        )